# Elastic Load Balancing offers two types of load balancers that both feature
# high availability, automatic scaling, and robust security. These include:
#
# - Classic Load Balancer that routes traffic based on either application or
#   network level information (L3)
# - Application Load Balancer that routes traffic based on advanced 
#   application level (L7) information that includes the content of the 
#   request. 
#
# The Classic Load Balancer is ideal for simple load balancing of traffic 
# across multiple EC2 instances, while the Application Load Balancer is ideal 
# for applications needing advanced routing capabilities, microservices, 
# and container-based architectures. Application Load Balancer offers ability 
# to route traffic to multiple services or load balance across multiple 
# ports on the same EC2 instance.
